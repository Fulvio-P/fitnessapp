<!--
Una variante della SideNav che non compare in alcuna delle route
che vengono elencate nell'array avoidroutes.
-->

<template>
  <div>
    <SideNav v-show="isCurrentRouteOk" />
  </div>
</template>

<script>
import Login from '../views/Login.vue'
import SideNav from "@/components/SideNavbar.vue"; //il nome del file Ã¨ diverso dal nome della componente
export default {
  name: "ShySideNavbar",
  props: ["avoidroutes"],
  extends: SideNav,
  computed: {
    isCurrentRouteOk() {
      return this.avoidroutes.every(r => r != Login.$route.path || r != this.$route.path); //programmazione funzionale <3
    }
  },
  components: {
    SideNav
  }
};
</script>

